package org.codegrinders.treasure_hunter_mobile;

public class Puzzle {
    private String id;
    private String question;
    private String answer;
    private int points;

    public String getId() {
        return id;
    }

    public String getQuestion() {
        return question;
    }

    public String getAnswer() {
        return answer;
    }

    public int getPoints() {
        return points;
    }
}
